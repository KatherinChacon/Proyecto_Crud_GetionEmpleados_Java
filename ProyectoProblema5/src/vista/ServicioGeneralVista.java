    private void btnCrearSActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCrearSActionPerformed

        Validaciones validaciones = new Validaciones();
        Encoder encoder = new Encoder();

        try {
            if (!txtNombreS.getText().trim().equals("") && !txtApellidoS.getText().trim().equals("") && !txtCedulaS.getText().trim().equals("")
                    && !txtSexoS.getText().trim().equals("") && !txtEstadoS.getText().trim().equals("") && !txtDireccionS.getText().trim().equals("")
                    && !txtCargoS.getText().trim().equals("") && !txtUsuarioS.getText().trim().equals("") && !txtContrasenaS.getText().trim().equals("")
                    && !txtAreaS.getText().trim().equals("")) {

                if (validaciones.ValidarEmail(txtCorreoS.getText())) {
                    if (txtTelefonoS.getText().length() >= 7){                    

                        Empleado empleado = new Empleado();
                        EmpleadoControlador empleadoControlador = new EmpleadoControlador();

                        empleado.setNombre(txtNombreS.getText());
                        empleado.setApellido(txtApellidoS.getText());
                        empleado.setCedula(Integer.parseInt(txtCedulaS.getText()));
                        empleado.setSexo(txtSexoS.getText());
                        empleado.setEstado(txtEstadoS.getText());
                        empleado.setDireccion(txtDireccionS.getText());
                        empleado.setTelefono(txtTelefonoS.getText());
                        empleado.setCorreo(txtCorreoS.getText());
                        empleado.setCargo(txtCargoS.getText());
                        empleado.setUsuario(txtUsuarioS.getText());
                        empleado.setContrasena(encoder.encrypt(txtContrasenaS.getText()));

                        ServicioGeneral servicio = new ServicioGeneral();
                        ServicioControlador servicioControlador = new ServicioControlador();

                        servicio.setAreaTrabajo(txtAreaS.getText());

                        boolean flag = empleadoControlador.crearUsuario(empleado);
                        if (flag) {
                            JOptionPane.showMessageDialog(null, "Usuario creado con Exito");
                            ServicioGeneralVista servicioGeneralVista = new ServicioGeneralVista();
                            servicioGeneralVista.setVisible(true);
                            this.dispose();
                        }

                        boolean flag4 = servicioControlador.crearUsuario(servicio);
                        if (flag4) {
                            ServicioGeneralVista servicioGeneralVista = new ServicioGeneralVista();
                            servicioGeneralVista.setVisible(true);
                            this.dispose();
                        }

                    } else {
                        JOptionPane.showMessageDialog(null, "El telefono debe tener minimo 7 digitos");
                    }
                } else {
                    JOptionPane.showMessageDialog(null, "El Correo debe tener un formato (ejemplo@xxxx.xx)");
                }
            } else {
                JOptionPane.showMessageDialog(null, "Diligencia todos los campos");
            }
        } catch (Exception ex) {
            Logger.getLogger(CoordinadorVista.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_btnCrearSActionPerformed

