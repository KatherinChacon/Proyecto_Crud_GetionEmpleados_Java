    private void mapearEmpleado() throws SQLException {
        EmpleadoControlador empleadoControlador = new EmpleadoControlador();
        Empleado empleado = new Empleado();
        empleado = empleadoControlador.consultarEmpleadoid(this.IdEmpleado);

        ServicioControlador servicioControlador = new ServicioControlador();
        ServicioGeneral servicio = new ServicioGeneral();
        servicio = (ServicioGeneral) servicioControlador.consultarEmpleadoid(this.IdEmpleado);

        int cedula = Integer.valueOf(empleado.getCedula());

        txtNombreS.setText(empleado.getNombre());
        txtApellidoS.setText(empleado.getApellido());
        txtCedulaS.setText(String.valueOf(cedula));
        txtSexoS.setText(empleado.getSexo());
        txtEstadoS.setText(empleado.getEstado());
        txtDireccionS.setText(empleado.getDireccion());
        txtTelefonoS.setText(empleado.getTelefono());
        txtCorreoS.setText(empleado.getCorreo());
        txtCargoS.setText(empleado.getCargo());
        txtUsuarioS.setText(empleado.getUsuario());
        txtContrasenaS.setText(empleado.getContrasena());
        txtAreaS.setText(servicio.getAreaTrabajo());
    }

