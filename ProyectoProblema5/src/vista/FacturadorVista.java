    private void btnCrearFActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCrearFActionPerformed
        Validaciones validaciones = new Validaciones();
        Encoder encoder = new Encoder();
        try {
            if (!txtNombreF.getText().trim().equals("") && !txtApellidoF.getText().trim().equals("") && !txtTelefonoF.getText().trim().equals("")
                    && !txtSexoF.getText().trim().equals("") && !txtEstadoF.getText().trim().equals("") && !txtDireccionF.getText().trim().equals("")
                    && !txtCargoF.getText().trim().equals("") && !txtUsuarioF.getText().trim().equals("") && !txtContrasenaF.getText().trim().equals("")
                    && !txtAreaF.getText().trim().equals("")) {

                if (validaciones.ValidarEmail(txtCorreoF.getText())) {

                    if (txtTelefonoF.getText().length() >= 7) {

                        Empleado empleado = new Empleado();

                        EmpleadoControlador empleadoControlador = new EmpleadoControlador();

                        empleado.setNombre(txtNombreF.getText());
                        empleado.setApellido(txtApellidoF.getText());
                        empleado.setCedula(Integer.parseInt(txtCedulaF.getText()));
                        empleado.setSexo(txtSexoF.getText());
                        empleado.setEstado(txtEstadoF.getText());
                        empleado.setDireccion(txtDireccionF.getText());
                        empleado.setTelefono(txtTelefonoF.getText());
                        empleado.setCorreo(txtCorreoF.getText());
                        empleado.setCargo(txtCargoF.getText());
                        empleado.setUsuario(txtUsuarioF.getText());
                        empleado.setContrasena(encoder.encrypt(txtContrasenaF.getText()));;

                        Facturador facturador = new Facturador();

                        FacturadorControlador facturadorControlador = new FacturadorControlador();

                        facturador.setAreaAsignada(txtAreaF.getText());

                        boolean flag = empleadoControlador.crearUsuario(empleado);
                        if (flag) {
                            JOptionPane.showMessageDialog(null, "Usuario creado con Exito");
                            FacturadorVista facturadorVista = new FacturadorVista();
                            facturadorVista.setVisible(true);
                            this.dispose();
                        }

                        boolean flag3 = facturadorControlador.crearUsuario(facturador);
                        if (flag3) {
                            FacturadorVista facturadorVista = new FacturadorVista();
                            facturadorVista.setVisible(true);
                            this.dispose();
                        }
                    } else {
                        JOptionPane.showMessageDialog(null, "El telefono debe tener minimo 7 digitos");
                    }
                } else {
                    JOptionPane.showMessageDialog(null, "El Correo debe tener un formato (ejemplo@xxxx.xx)");
                }
            } else {
                JOptionPane.showMessageDialog(null, "Diligencia todos los campos");
            }
        } catch (Exception ex) {
            Logger.getLogger(CoordinadorVista.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_btnCrearFActionPerformed

