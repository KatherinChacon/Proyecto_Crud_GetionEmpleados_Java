    private void mapearEmpleado() throws SQLException {
        EmpleadoControlador empleadoControlador = new EmpleadoControlador();
        Empleado empleado = new Empleado();
        empleado = empleadoControlador.consultarEmpleadoid(this.IdEmpleado);

        FacturadorControlador facturadorControlador = new FacturadorControlador();
        Facturador facturador = new Facturador();
        facturador = (Facturador) facturadorControlador.consultarEmpleadoid(this.IdEmpleado);

        int cedula = Integer.valueOf(empleado.getCedula());

        txtNombreF.setText(empleado.getNombre());
        txtApellidoF.setText(empleado.getApellido());
        txtCedulaF.setText(String.valueOf(cedula));
        txtSexoF.setText(empleado.getSexo());
        txtEstadoF.setText(empleado.getEstado());
        txtDireccionF.setText(empleado.getDireccion());
        txtTelefonoF.setText(empleado.getTelefono());
        txtCorreoF.setText(empleado.getCorreo());
        txtCargoF.setText(empleado.getCargo());
        txtUsuarioF.setText(empleado.getUsuario());
        txtContrasenaF.setText(empleado.getContrasena());
        txtAreaF.setText(facturador.getAreaAsignada());
    }

